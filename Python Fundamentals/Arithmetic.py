# Python arithmetic operators 


# Addition "+"
# add, 1 + 2
print(1+2) # using print statement to display the output 


# Subtraction "-"
# Subtract, 2 - 1
print(2-1)


# Division 
# "/"
# 2/1
print(2/1) # Note *Division will produce float value 

# Floor division
# //
print(11//2) # Will produce an int


# Multiplication
# *
print(1*3) # Will produce an int

# Exponentiation
# **
print(2*3) 

# Modulus
# %
print(20 % 6)


